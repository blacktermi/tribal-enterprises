/**
 * TRIBAL LLM - Model Registry
 *
 * Registre des modeles disponibles via OpenRouter.
 * Architecture extensible : chaque provider peut avoir son propre catalogue.
 */

export interface LlmModelInfo {
  id: string // e.g. "openai/gpt-4o"
  name: string // e.g. "GPT-4o"
  provider: string // e.g. "OpenAI"
  providerSlug: string // e.g. "openrouter"
  description: string
  contextWindow: number // tokens
  maxOutput?: number
  inputPrice: number // $ per 1M tokens
  outputPrice: number // $ per 1M tokens
  category: LlmModelCategory
  capabilities: LlmCapability[]
  isDefault?: boolean
  isFeatured?: boolean
  iconColor?: string
}

export type LlmModelCategory =
  | 'flagship'
  | 'fast'
  | 'reasoning'
  | 'coding'
  | 'creative'
  | 'vision'
  | 'free'
  | 'open-source'

export type LlmCapability =
  | 'chat'
  | 'code'
  | 'vision'
  | 'function-calling'
  | 'json-mode'
  | 'reasoning'
  | 'long-context'

