/**
 * Tous les modeles disponibles (OpenRouter + GitHub)
 */
export const ALL_MODELS: LlmModelInfo[] = [...OPENROUTER_MODELS, ...GITHUB_MODELS]

/**
 * Obtenir le modele par defaut
 */
export function getDefaultModel(): LlmModelInfo {
  return OPENROUTER_MODELS.find(m => m.isDefault) || OPENROUTER_MODELS[0]
}

/**
 * Trouver un modele par son ID
 */
export function findModelById(id: string): LlmModelInfo | undefined {
  return ALL_MODELS.find(m => m.id === id)
}

/**
 * Obtenir les modeles d'une categorie
 */
export function getModelsByCategory(category: LlmModelCategory): LlmModelInfo[] {
  return ALL_MODELS.filter(m => m.category === category)
}

/**
 * Obtenir les modeles mis en avant
 */
export function getFeaturedModels(): LlmModelInfo[] {
  return ALL_MODELS.filter(m => m.isFeatured)
}

/**
 * Formater le prix pour affichage
 */
export function formatPrice(pricePerMillion: number): string {
  if (pricePerMillion === 0) return 'Gratuit'
  if (pricePerMillion < 0.01) return `$${pricePerMillion.toFixed(4)}/1M`
  if (pricePerMillion < 1) return `$${pricePerMillion.toFixed(2)}/1M`
  return `$${pricePerMillion.toFixed(1)}/1M`
}

/**
 * Estimer le cout d'un message
 */
export function estimateCost(
  model: LlmModelInfo,
  inputTokens: number,
  outputTokens: number
): number {
  return (inputTokens * model.inputPrice + outputTokens * model.outputPrice) / 1_000_000
}
